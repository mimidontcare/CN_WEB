const Nguoidung = require("../models/NguoidungModel");

exports.getAll = (req, res) => {
  Nguoidung.getAll((err, results) => {
    if (err) return res.status(500).json(err);
    res.json(results);
  });
};

exports.getById = (req, res) => {
  Nguoidung.getById(req.params.id, (err, result) => {
    if (err) return res.status(500).json(err);
    res.json(result);
  });
};

exports.create = (req, res) => {
  Nguoidung.create(req.body, (err) => {
    if (err) return res.status(500).json(err);
    res.json({ message: "Thêm thành công" });
  });
};

exports.update = (req, res) => {
  Nguoidung.update(req.params.id, req.body, (err) => {
    if (err) return res.status(500).json(err);
    res.json({ message: "Cập nhật thành công" });
  });
};

exports.delete = (req, res) => {
  Nguoidung.delete(req.params.id, (err) => {
    if (err) return res.status(500).json(err);
    res.json({ message: "Xoá thành công" });
  });
};
exports.getByEmail = (req, res) => {
  Nguoidung.getByEmail(req.params.email, (err, result) => {
    if (err) return res.status(500).json(err);
    res.json(result);
  });
};
